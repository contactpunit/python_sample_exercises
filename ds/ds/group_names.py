def group_names_by_country(data: str = data) -> defaultdict:
    countries = defaultdict(list)
    _, remain = data.split('\n', 1)
    for entry in remain.split('\n'):
        lname, fname, country = entry.strip().split(',')
        countries[country].append(f'{fname} {lname}')
    return countries
